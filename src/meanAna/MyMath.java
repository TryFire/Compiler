public class MyMath {
    public double sin(Double para) {
        return Math.sin(para);
    }
    public double cos(Double para) {
        return Math.cos(para);
    }
    public double tan(Double para) {
        return Math.tan(para);
    }
    public double sqrt(Double para) {
        return Math.sqrt(para);
    }
    public double exp(Double para) {
        return Math.exp(para);
    }
    public double ln(Double para) {
        return Math.log1p(para);
    }
}
